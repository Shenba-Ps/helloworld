class Elseif{
    public static void main(String[] args) {
        int a=1000;
     if (a<=2000){
         System.out.println("a is less than or equal to 2000");
     }
     else if (a>=1500){
         System.out.println("a is greater than or equal to 1500");
     }
     else if (a==2000) {
         System.out.println("a is equal to 2000");
     }
     else if (a!=1000){
             System.out.println("a is not equal to 1000");
         }

    }
}